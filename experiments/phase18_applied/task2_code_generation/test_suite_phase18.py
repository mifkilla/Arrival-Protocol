# Test 10: GET /tasks?search=meeting — Search
# ============================================================
def test_10_search(client):
    """Test case-insensitive search in title and description."""
    client.post("/tasks", json={"title": "Team Meeting", "description": "Weekly sync"})
    client.post("/tasks", json={"title": "Buy food", "description": "For the meeting lunch"})
    client.post("/tasks", json={"title": "Unrelated task"})

    response = client.get("/tasks?search=meeting")
    assert response.status_code == 200
    data = response.json()
    tasks = data.get("tasks", data) if isinstance(data, dict) else data
    assert len(tasks) >= 2, f"Expected at least 2 results for 'meeting', got {len(tasks)}"
    for task in tasks:
        combined = (task.get("title", "") + " " + task.get("description", "")).lower()
        assert "meeting" in combined, f"Task doesn't match 'meeting': {task}"


