# Test 15: Integration — multiple operations in sequence
# ============================================================
def test_15_integration_workflow(client):
    """Test a complete CRUD workflow: create → read → update → verify → delete → verify."""
    # Create
    resp1 = client.post("/tasks", json={
        "title": "Integration test task",
        "priority": "low",
        "status": "todo",
        "tags": ["test"]
    })
    assert resp1.status_code == 201
    task_id = resp1.json()["id"]

    # Read
    resp2 = client.get(f"/tasks/{task_id}")
    assert resp2.status_code == 200
    assert resp2.json()["title"] == "Integration test task"
    assert resp2.json()["status"] == "todo"

    # Update
    resp3 = client.put(f"/tasks/{task_id}", json={
        "title": "Updated integration task",
        "priority": "critical",
        "status": "done",
        "tags": ["test", "completed"]
    })
    assert resp3.status_code == 200
    assert resp3.json()["title"] == "Updated integration task"
    assert resp3.json()["status"] == "done"

    # Verify update persisted
    resp4 = client.get(f"/tasks/{task_id}")
    assert resp4.status_code == 200
    assert resp4.json()["priority"] == "critical"
    assert "completed" in resp4.json()["tags"]

    # Delete
    resp5 = client.delete(f"/tasks/{task_id}")
    assert resp5.status_code in (200, 204)

    # Verify deleted
    resp6 = client.get(f"/tasks/{task_id}")
    assert resp6.status_code == 404
